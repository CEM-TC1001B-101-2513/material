variable = 10
print(type(variable)) # int -> integer -> número entero
print(type(10.5)) # float -> número de punto flotante -> número real
print(type(5j)) # complex -> número complejo
print(type("hola")) # str -> string -> texto
print(type(True)) # bool -> booleano
print(type(False))
print(type([1,2,"hola"])) # list -> conjunto ordenado y mutable de datos
print(type((1,2,"hola"))) # tuple -> conjunto ordenado e inmutable de datos
print(type({"uno": 5, "dos": 10})) # dict -> diccionario

